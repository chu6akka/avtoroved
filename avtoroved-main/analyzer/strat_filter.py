def is_literary_override(lemma: str, layer: str) -> bool:
    """Вернуть True если слово следует считать литературной нормой.

    Применяется только к «слабым» слоям (colloquial_reduced, general_jargon и др.)
    """
    if layer not in FILTERABLE_LAYERS:
        return False
    return lemma.lower() in LITERARY_WHITELIST


def filter_stratified_tokens(tokens: list) -> list:
    """Убрать из результатов стратификации токены-ложные срабатывания."""
    return [t for t in tokens if not is_literary_override(t.lemma, t.layer)]
