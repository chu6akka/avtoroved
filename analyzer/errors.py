"""
analyzer/errors.py — Модуль анализа ошибок письменной речи
============================================================
Включает:
  - Пунктуационные проверки (на основе «Правил русской орфографии и пунктуации» 1956 г.)
  - Орфографические проверки
  - Грамматические проверки
  - Лексико-фразеологические проверки
  - Оценку степеней развития навыков (по С.М. Вул)
  - Профиль интернет-коммуникации (по Колокольцевой)

Методическая основа:
  С.М. Вул «Судебно-автороведческая идентификационная экспертиза» (2007)
  «Правила русской орфографии и пунктуации» (утв. АН СССР, 1956)
"""

from __future__ import annotations
import re
from dataclasses import dataclass, field
from typing import List, Tuple, Dict, Optional


# ============================================================================
# ДАТАКЛАССЫ
# ============================================================================
@dataclass
class TextError:
    """Единичная ошибка в тексте."""
    error_type: str       # Пунктуационная / Орфографическая / Грамматическая / Лексическая
    subtype: str          # Подтип ошибки
    fragment: str         # Фрагмент текста с ошибкой
    description: str      # Описание ошибки
    suggestion: str       # Рекомендация по исправлению
    position: Tuple[int, int] = (0, 0)  # (start, end) в тексте
    rule_ref: str = ""    # Ссылка на параграф правил


@dataclass
class SkillLevel:
    """Степень развития языкового навыка."""
    skill_name: str       # Название навыка
    level: str            # высокая / средняя / низкая / нулевая
    description: str      # Пояснение
    error_count: int = 0
    error_rate: float = 0.0


@dataclass
class InternetProfile:
    """Профиль интернет-коммуникации."""
    internet_comm_score: float = 0.0
    emoji_count: int = 0
    emoticon_count: int = 0
    hashtag_count: int = 0
    mention_count: int = 0
    url_count: int = 0
    caps_words: int = 0
    slang_count: int = 0
    abbreviation_count: int = 0
    repeated_punct_count: int = 0
    missing_caps_sentences: int = 0
    features: Dict[str, int] = field(default_factory=dict)


@dataclass
class ErrorAnalysisResult:
    """Результат полного анализа ошибок."""
    errors: List[TextError]
    skill_levels: List[SkillLevel]
    internet_profile: InternetProfile
    total_words: int = 0
    total_sentences: int = 0


