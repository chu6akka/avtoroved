def format_error_report(result: ErrorAnalysisResult) -> str:
    """Форматирование отчёта об ошибках в текстовом виде."""
    lines = []
    lines.append("=" * 60)
    lines.append("АНАЛИЗ ОШИБОК ПИСЬМЕННОЙ РЕЧИ")
    lines.append("=" * 60)
    lines.append("")

    # Степени развития навыков
    lines.append("СТЕПЕНИ РАЗВИТИЯ НАВЫКОВ (по С.М. Вул)")
    lines.append("-" * 40)
    for skill in result.skill_levels:
        lines.append(f"  {skill.skill_name}: {skill.level.upper()}")
        lines.append(f"    {skill.description}")
    lines.append("")

    # Ошибки по типам
    by_type: Dict[str, List[TextError]] = {}
    for e in result.errors:
        by_type.setdefault(e.error_type, []).append(e)

    lines.append(f"ВСЕГО ОШИБОК: {len(result.errors)}")
    lines.append("-" * 40)

    for etype in ["Пунктуационная", "Орфографическая", "Грамматическая", "Лексическая"]:
        errs = by_type.get(etype, [])
        lines.append(f"\n{etype.upper()} ОШИБКИ: {len(errs)}")
        for i, e in enumerate(errs[:15], 1):
            lines.append(f"  {i}. «{e.fragment}»")
            lines.append(f"     {e.description}")
            if e.suggestion:
                lines.append(f"     → {e.suggestion}")
            if e.rule_ref:
                lines.append(f"     [{e.rule_ref}]")

    return "\n".join(lines)


def format_internet_profile(profile: InternetProfile) -> str:
    """Форматирование профиля интернет-коммуникации."""
    lines = []
    lines.append("=" * 60)
    lines.append("ПРОФИЛЬ ИНТЕРНЕТ-КОММУНИКАЦИИ")
    lines.append("=" * 60)
    lines.append("")

    score_pct = round(profile.internet_comm_score * 100, 1)
    if score_pct >= 50:
        assessment = "ВЫСОКАЯ степень характерности интернет-коммуникации"
    elif score_pct >= 25:
        assessment = "СРЕДНЯЯ степень характерности интернет-коммуникации"
    elif score_pct >= 10:
        assessment = "НИЗКАЯ степень характерности интернет-коммуникации"
    else:
        assessment = "МИНИМАЛЬНАЯ степень характерности интернет-коммуникации"

    lines.append(f"  Оценка: {score_pct}% — {assessment}")
    lines.append("")
    lines.append("  Обнаруженные признаки:")
    lines.append(f"    Эмодзи: {profile.emoji_count}")
    lines.append(f"    Текстовые смайлики: {profile.emoticon_count}")
    lines.append(f"    Хештеги: {profile.hashtag_count}")
    lines.append(f"    Упоминания (@): {profile.mention_count}")
    lines.append(f"    Ссылки (URL): {profile.url_count}")
    lines.append(f"    Слова КАПСЛОКОМ: {profile.caps_words}")
    lines.append(f"    Интернет-сленг: {profile.slang_count}")
    lines.append(f"    Аббревиатуры: {profile.abbreviation_count}")
    lines.append(f"    Повторяющаяся пунктуация: {profile.repeated_punct_count}")
    lines.append(f"    Предложения без прописной: {profile.missing_caps_sentences}")

    lines.append("")
    lines.append("  Вывод для автороведческой экспертизы:")
    if score_pct >= 50:
        lines.append("    Текст содержит многочисленные признаки интернет-коммуникации,")
        lines.append("    что указывает на привычную среду общения автора.")
    elif score_pct >= 25:
        lines.append("    Текст содержит заметные признаки интернет-коммуникации.")
    elif score_pct >= 10:
        lines.append("    В тексте присутствуют отдельные элементы интернет-коммуникации.")
    else:
        lines.append("    Текст не содержит выраженных признаков интернет-коммуникации.")

    return "\n".join(lines)
