def analyze_internet_communication(text: str) -> InternetProfile:
    """Анализ признаков интернет-коммуникации в тексте."""
    profile = InternetProfile()

    # Эмодзи
    emoji_matches = EMOJI_PATTERN.findall(text)
    profile.emoji_count = len(emoji_matches)

    # Текстовые смайлики
    emoticon_matches = EMOTICON_PATTERN.findall(text)
    profile.emoticon_count = len(emoticon_matches)

    # Хештеги
    hashtag_matches = HASHTAG_PATTERN.findall(text)
    profile.hashtag_count = len(hashtag_matches)

    # Упоминания (@)
    mention_matches = MENTION_PATTERN.findall(text)
    profile.mention_count = len(mention_matches)

    # URL
    url_matches = URL_PATTERN.findall(text)
    profile.url_count = len(url_matches)

    # КАПСЛОК (слова из 3+ заглавных букв)
    caps_words = re.findall(r'\b[А-ЯЁA-Z]{3,}\b', text)
    # Исключаем аббревиатуры и начала предложений
    caps_filtered = [w for w in caps_words if len(w) > 3 or w.lower() not in ['все', 'для', 'это']]
    profile.caps_words = len(caps_filtered)

    # Интернет-сленг
    lower = text.lower()
    slang_count = 0
    for slang in INTERNET_SLANG:
        slang_count += len(re.findall(r'(?<!\w)' + re.escape(slang) + r'(?!\w)', lower))
    profile.slang_count = slang_count

    # Аббревиатуры интернет-общения
    abbr_count = 0
    for abbr in INTERNET_ABBREVIATIONS:
        abbr_count += len(re.findall(r'(?<!\w)' + re.escape(abbr) + r'(?!\w)', lower))
    profile.abbreviation_count = abbr_count

    # Повторяющаяся пунктуация
    repeated_matches = REPEATED_PUNCT_PATTERN.findall(text)
    profile.repeated_punct_count = len(repeated_matches)

    # Предложения без прописной буквы
    sentences = re.split(r'[.!?]+\s+', text)
    missing_caps = sum(1 for s in sentences if s and s[0].islower())
    profile.missing_caps_sentences = missing_caps

    # Итоговый балл
    words = re.findall(r'[А-Яа-яЁёA-Za-z]+', text)
    total_words = max(len(words), 1)

    features = {
        'emoji': profile.emoji_count,
        'emoticons': profile.emoticon_count,
        'hashtags': profile.hashtag_count,
        'mentions': profile.mention_count,
        'urls': profile.url_count,
        'caps': profile.caps_words,
        'slang': profile.slang_count,
        'abbreviations': profile.abbreviation_count,
        'repeated_punct': profile.repeated_punct_count,
        'missing_caps': profile.missing_caps_sentences,
    }
    profile.features = features

    # Взвешенная оценка (0..1)
    weights = {
        'emoji': 3.0, 'emoticons': 2.5, 'hashtags': 2.0, 'mentions': 2.0,
        'urls': 1.0, 'caps': 1.5, 'slang': 3.0, 'abbreviations': 2.0,
        'repeated_punct': 1.5, 'missing_caps': 1.0,
    }
    raw_score = sum(features[k] * weights[k] for k in features) / total_words
    profile.internet_comm_score = min(1.0, raw_score * 5)

    return profile


