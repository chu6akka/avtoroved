class ErrorAnalyzer:
    """Анализатор ошибок письменной речи для автороведческой экспертизы."""

    def __init__(self):
        self._spelling_dict = COMMON_SPELLING_ERRORS
        self._introductory_words = set(INTRODUCTORY_WORDS)

    def analyze(self, text: str) -> ErrorAnalysisResult:
        """Полный анализ текста."""
        errors: List[TextError] = []
        words = re.findall(r'[А-Яа-яЁё]+', text)
        sentences = [s.strip() for s in re.split(r'[.!?]+', text) if s.strip()]
        total_words = len(words)
        total_sentences = len(sentences)

        # 1. Пунктуационные ошибки
        punct_errors = self._check_punctuation(text)
        errors.extend(punct_errors)

        # 2. Орфографические ошибки
        spelling_errors = self._check_spelling(text)
        errors.extend(spelling_errors)

        # 3. Грамматические ошибки
        grammar_errors = self._check_grammar(text)
        errors.extend(grammar_errors)

        # 4. Лексико-фразеологические ошибки
        lexical_errors = self._check_lexical(text)
        errors.extend(lexical_errors)

        # 5. Определение степеней развития навыков
        skill_levels = self._assess_skills(errors, total_words)

        # 6. Профиль интернет-коммуникации
        internet_profile = analyze_internet_communication(text)

        return ErrorAnalysisResult(
            errors=errors,
            skill_levels=skill_levels,
            internet_profile=internet_profile,
            total_words=total_words,
            total_sentences=total_sentences,
        )

    # --- ПУНКТУАЦИЯ ---
    def _check_punctuation(self, text: str) -> List[TextError]:
        errors = []

        # Проверка оформления знаков препинания (§198-203)
        for pattern, subtype, name, desc in COMBINED_PUNCT_PATTERNS:
            for m in re.finditer(pattern, text):
                errors.append(TextError(
                    error_type="Пунктуационная",
                    subtype=name,
                    fragment=m.group()[:40],
                    description=desc,
                    suggestion="Исправьте оформление знаков препинания",
                    position=(m.start(), m.end()),
                    rule_ref="§198-203"
                ))

        # Проверка запятых (§136-149, §153-155)
        for pattern, subtype, name, desc in COMMA_ERROR_PATTERNS:
            for m in re.finditer(pattern, text, re.IGNORECASE):
                # Проверяем, нет ли уже запятой перед этим словом
                before = text[max(0, m.start()-2):m.start()]
                if ',' in before:
                    continue
                errors.append(TextError(
                    error_type="Пунктуационная",
                    subtype=name,
                    fragment=text[max(0, m.start()-5):min(len(text), m.end()+5)],
                    description=desc,
                    suggestion="Проверьте наличие запятой",
                    position=(m.start(), m.end()),
                    rule_ref="§136-155"
                ))

        # Проверка вводных слов (§155)
        self._check_introductory_words(text, errors)

        # Проверка кавычек (§192-194)
        self._check_quotes(text, errors)

        # Проверка многоточия (§185-187)
        for pattern, subtype, name, desc in ELLIPSIS_PATTERNS:
            for m in re.finditer(pattern, text):
                errors.append(TextError(
                    error_type="Пунктуационная",
                    subtype=name,
                    fragment=m.group(),
                    description=desc,
                    suggestion="Используйте стандартное многоточие (три точки)",
                    position=(m.start(), m.end()),
                    rule_ref="§185"
                ))

        # Проверка тире vs дефис (§164-179)
        self._check_dashes(text, errors)

        # Проверка пробелов вокруг тире (§164)
        for m in re.finditer(r'[А-Яа-яЁё]—[А-Яа-яЁё]', text):
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="оформление_тире",
                fragment=m.group(),
                description="Тире должно быть отделено пробелами с обеих сторон",
                suggestion="Добавьте пробелы вокруг тире",
                position=(m.start(), m.end()),
                rule_ref="§164"
            ))

        # Проверка точки в конце текста (§125)
        stripped = text.rstrip()
        if stripped and stripped[-1] not in '.!?…»"\')"':
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="отсутствие_конечного_знака",
                fragment=stripped[-20:],
                description="Текст не завершён конечным знаком препинания (§125)",
                suggestion="Поставьте точку, вопросительный или восклицательный знак",
                position=(max(0, len(stripped)-1), len(stripped)),
                rule_ref="§125"
            ))

        # Проверка прописной буквы после точки (§92)
        for m in re.finditer(r'[.!?]\s+([а-яё])', text):
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="строчная_после_точки",
                fragment=text[m.start():m.end()],
                description="После точки/!/?/предложение начинается со строчной буквы (§92)",
                suggestion="Начните предложение с прописной буквы",
                position=(m.start(), m.end()),
                rule_ref="§92"
            ))

        # Проверка скобок (§188-191)
        self._check_brackets(text, errors)

        return errors

    def _check_introductory_words(self, text: str, errors: List[TextError]):
        """Проверка обособления вводных слов (§155)."""
        lower = text.lower()
        for word in self._introductory_words:
            for m in re.finditer(r'(?<!\w)' + re.escape(word) + r'(?!\w)', lower):
                start, end = m.start(), m.end()
                # Проверяем, обособлено ли запятыми
                before = text[max(0, start-2):start].strip()
                after = text[end:min(len(text), end+2)].strip()

                # Вводное слово в начале предложения — запятая после
                if (not before or before[-1:] in '.!?') and after and after[0] != ',':
                    errors.append(TextError(
                        error_type="Пунктуационная",
                        subtype="вводное_слово",
                        fragment=text[start:min(len(text), end+10)],
                        description=f'Вводное слово «{word}» не обособлено запятой (§155)',
                        suggestion=f'Поставьте запятую после «{word}»',
                        position=(start, end),
                        rule_ref="§155"
                    ))
                # Вводное слово в середине предложения — запятые с обеих сторон
                elif before and before[-1:] not in ',.;:!?—' and after and after[0] != ',':
                    if before[-1:].isalpha():
                        errors.append(TextError(
                            error_type="Пунктуационная",
                            subtype="вводное_слово",
                            fragment=text[max(0,start-5):min(len(text), end+10)],
                            description=f'Вводное слово «{word}» не обособлено запятыми (§155)',
                            suggestion=f'Обособьте «{word}» запятыми с обеих сторон',
                            position=(start, end),
                            rule_ref="§155"
                        ))

    def _check_quotes(self, text: str, errors: List[TextError]):
        """Проверка сбалансированности кавычек (§192-194)."""
        # Кавычки-ёлочки
        open_q = text.count('«')
        close_q = text.count('»')
        if open_q != close_q:
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="кавычки",
                fragment=f'«={open_q}, »={close_q}',
                description=f'Несбалансированные кавычки-«ёлочки»: {open_q} откр., {close_q} закр. (§192)',
                suggestion='Проверьте парность кавычек',
                position=(0, 0),
                rule_ref="§192"
            ))

        # Кавычки-лапки
        open_l = text.count('"')
        # Проверяем программистские кавычки
        dq_count = text.count('"')
        if dq_count % 2 != 0:
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="кавычки",
                fragment=f'Непарное кол-во: {dq_count}',
                description='Несбалансированные кавычки (§192)',
                suggestion='Проверьте парность кавычек; в русском тексте используйте «ёлочки»',
                position=(0, 0),
                rule_ref="§192"
            ))

    def _check_dashes(self, text: str, errors: List[TextError]):
        """Проверка использования тире и дефисов (§164-179)."""
        # Дефис вместо тире между словами с пробелами
        for m in re.finditer(r'[а-яА-ЯЁё]\s+-\s+[а-яА-ЯЁё]', text):
            fragment = m.group()
            # Это скорее тире, чем дефис
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="дефис_вместо_тире",
                fragment=fragment,
                description='Использован дефис (-) вместо тире (—) между словами (§164)',
                suggestion='Замените дефис на тире: « — »',
                position=(m.start(), m.end()),
                rule_ref="§164"
            ))

    def _check_brackets(self, text: str, errors: List[TextError]):
        """Проверка парности скобок (§188-191)."""
        stack = []
        pairs = {'(': ')', '[': ']', '{': '}'}
        for i, ch in enumerate(text):
            if ch in pairs:
                stack.append((ch, i))
            elif ch in pairs.values():
                if stack and pairs.get(stack[-1][0]) == ch:
                    stack.pop()
                else:
                    errors.append(TextError(
                        error_type="Пунктуационная",
                        subtype="скобки",
                        fragment=ch,
                        description=f'Закрывающая скобка «{ch}» без открывающей (§188)',
                        suggestion='Проверьте парность скобок',
                        position=(i, i+1),
                        rule_ref="§188"
                    ))
        for ch, pos in stack:
            errors.append(TextError(
                error_type="Пунктуационная",
                subtype="скобки",
                fragment=ch,
                description=f'Незакрытая скобка «{ch}» (§188)',
                suggestion='Добавьте закрывающую скобку',
                position=(pos, pos+1),
                rule_ref="§188"
            ))

    # --- ОРФОГРАФИЯ ---
    def _check_spelling(self, text: str) -> List[TextError]:
        errors = []
        lower = text.lower()

        # Проверка по словарю частых ошибок
        for wrong, correct in self._spelling_dict.items():
            if wrong == correct:
                continue
            for m in re.finditer(r'(?<!\w)' + re.escape(wrong) + r'(?!\w)', lower):
                orig = text[m.start():m.end()]
                errors.append(TextError(
                    error_type="Орфографическая",
                    subtype="словарная",
                    fragment=orig,
                    description=f'Вероятная ошибка: «{orig}» → «{correct}»',
                    suggestion=f'Замените на «{correct}»',
                    position=(m.start(), m.end()),
                ))

        # Проверка по паттернам
        for pattern, subtype, desc, sugg in SPELLING_PATTERNS:
            if desc is None:
                continue
            for m in re.finditer(pattern, text, re.IGNORECASE):
                errors.append(TextError(
                    error_type="Орфографическая",
                    subtype=subtype,
                    fragment=m.group()[:40],
                    description=desc,
                    suggestion=sugg or "",
                    position=(m.start(), m.end()),
                ))

        return errors

    # --- ГРАММАТИКА ---
    def _check_grammar(self, text: str) -> List[TextError]:
        errors = []
        for pattern, subtype, desc, sugg in GRAMMAR_PATTERNS:
            for m in re.finditer(pattern, text, re.IGNORECASE):
                errors.append(TextError(
                    error_type="Грамматическая",
                    subtype=subtype,
                    fragment=m.group()[:40],
                    description=desc,
                    suggestion=sugg,
                    position=(m.start(), m.end()),
                ))
        return errors

    # --- ЛЕКСИКА ---
    def _check_lexical(self, text: str) -> List[TextError]:
        errors = []
        lower = text.lower()

        # Паронимы (информационно)
        for word, (correct, desc) in PARONYMS.items():
            for m in re.finditer(r'(?<!\w)' + re.escape(word) + r'(?!\w)', lower):
                errors.append(TextError(
                    error_type="Лексическая",
                    subtype="пароним",
                    fragment=text[m.start():m.end()],
                    description=f'Проверьте: {desc}',
                    suggestion=f'Не перепутаны ли «{word}» и «{correct}»?',
                    position=(m.start(), m.end()),
                ))

        # Канцеляризмы
        for phrase in CLERICISMS:
            for m in re.finditer(r'(?<!\w)' + re.escape(phrase) + r'(?!\w)', lower):
                errors.append(TextError(
                    error_type="Лексическая",
                    subtype="канцеляризм",
                    fragment=text[m.start():m.end()],
                    description=f'Канцеляризм/штамп: «{phrase}»',
                    suggestion='Рассмотрите более живую формулировку',
                    position=(m.start(), m.end()),
                ))

        # Просторечия
        for word in COLLOQUIALISMS:
            for m in re.finditer(r'(?<!\w)' + re.escape(word) + r'(?!\w)', lower):
                errors.append(TextError(
                    error_type="Лексическая",
                    subtype="просторечие",
                    fragment=text[m.start():m.end()],
                    description=f'Просторечная форма: «{word}»',
                    suggestion='Замените на литературный вариант',
                    position=(m.start(), m.end()),
                ))

        # Тавтология (повтор однокоренных слов рядом)
        words = re.findall(r'[А-Яа-яЁё]{4,}', text)
        for i in range(len(words) - 1):
            w1, w2 = words[i].lower(), words[i+1].lower()
            # Грубая проверка: совпадение первых 4 букв
            if len(w1) >= 4 and len(w2) >= 4 and w1[:4] == w2[:4] and w1 != w2:
                errors.append(TextError(
                    error_type="Лексическая",
                    subtype="тавтология",
                    fragment=f'{words[i]} {words[i+1]}',
                    description=f'Возможная тавтология: «{words[i]}» и «{words[i+1]}»',
                    suggestion='Замените одно из слов синонимом',
                    position=(0, 0),
                ))

        return errors

    # --- ОЦЕНКА НАВЫКОВ ---
    def _assess_skills(self, errors: List[TextError], total_words: int) -> List[SkillLevel]:
        """Оценка степеней развития навыков по С.М. Вул."""
        if total_words == 0:
            return [
                SkillLevel("Пунктуационные навыки", "нулевая", "Нет текста для анализа"),
                SkillLevel("Орфографические навыки", "нулевая", "Нет текста для анализа"),
                SkillLevel("Грамматические навыки", "нулевая", "Нет текста для анализа"),
                SkillLevel("Лексико-фразеологические навыки", "нулевая", "Нет текста для анализа"),
            ]

        # Подсчёт ошибок по типам
        punct_errors = [e for e in errors if e.error_type == "Пунктуационная"]
        spell_errors = [e for e in errors if e.error_type == "Орфографическая"]
        gram_errors = [e for e in errors if e.error_type == "Грамматическая"]
        lex_errors = [e for e in errors if e.error_type == "Лексическая"]

        # Нормирование на 200 слов (по методике Вул)
        norm = 200 / max(total_words, 1)

        def _rate(count, thresholds=(1, 3, 7)):
            """Оценка: высокая (0-1), средняя (2-3), низкая (4+)."""
            normed = count * norm
            if normed <= thresholds[0]:
                return "высокая", f"{count} ош. (≈{normed:.1f} на 200 слов)"
            elif normed <= thresholds[1]:
                return "средняя", f"{count} ош. (≈{normed:.1f} на 200 слов)"
            elif normed <= thresholds[2]:
                return "низкая", f"{count} ош. (≈{normed:.1f} на 200 слов)"
            else:
                return "нулевая", f"{count} ош. (≈{normed:.1f} на 200 слов)"

        levels = []
        for name, errs, thresholds in [
            ("Пунктуационные навыки", punct_errors, (2, 5, 10)),
            ("Орфографические навыки", spell_errors, (1, 3, 6)),
            ("Грамматические навыки", gram_errors, (1, 3, 7)),
            ("Лексико-фразеологические навыки", lex_errors, (2, 5, 10)),
        ]:
            level, desc = _rate(len(errs), thresholds)
            levels.append(SkillLevel(
                skill_name=name,
                level=level,
                description=desc,
                error_count=len(errs),
                error_rate=len(errs) * norm,
            ))

        return levels


